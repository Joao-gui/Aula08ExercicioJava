package com.db1.db1start;

public class Soma_Subtracao{
	public static int operacao_soma(Integer numero1, Integer numero2){
		Integer soma;
		soma = numero1 + numero2;		
		return soma;
	}
	public static int operacao_subtracao(Integer numero1, Integer numero2){
		Integer subtracao;
		subtracao = numero1 - numero2;		
		return subtracao;
	}

}
